// 操作符
#include <stdio.h>

int main()
{
  /* code */
  // 单目操作符
  // 双目操作符
  // 三目操作符

  int a = 10;
  int b = 0;
  a + b; // + 双目操作符

  // c语言中： 0 - 假， 非0 - 真
  printf(" %d\n", a);
  printf(" %d\n", !a);

  printf(" %d\n", b);
  printf(" %d\n", !b);

  int c = 10;
  int d = -c;

  printf(" %d\n", c);
  printf(" %d\n", d);

  return 0;
}
